    --interior-backfill
    --interior-knn-factor "${INTERIOR_KNN_FACTOR}"
    --interior-ellipsoid-factor "${INTERIOR_ELLIPSOID_FACTOR}"
    --interior-color-scale "${INTERIOR_COLOR_SCALE}"
