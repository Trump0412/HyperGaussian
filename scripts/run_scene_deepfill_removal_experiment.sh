    --mask-guided
    --mask-guided-target-count "${TARGET_GAUSSIANS}"
    --mask-guided-max-frames "${MASK_GUIDED_MAX_FRAMES}"
    --mask-guided-dilation-radius "${MASK_GUIDED_DILATION_RADIUS}"
