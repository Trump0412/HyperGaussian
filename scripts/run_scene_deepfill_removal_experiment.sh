    --mask-guided-vote-threshold "${MASK_GUIDED_VOTE_THRESHOLD}"
    --mask-guided-background-margin "${MASK_GUIDED_BACKGROUND_MARGIN}"
    --mask-guided-min-pos-votes "${MASK_GUIDED_MIN_POS_VOTES}"
