  EXPAND_ARGS+=(
    --cluster-refine
    --cluster-refine-target-count "${CLUSTER_REFINE_TARGET_COUNT}"
    --cluster-refine-radius-scale "${CLUSTER_REFINE_RADIUS_SCALE}"
